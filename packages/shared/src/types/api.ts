/**
 * API関連の共通型定義
 * 初学者向け: エラーハンドリングと成功レスポンスの統一
 */

/**
 * API成功レスポンスの基本形
 */
export interface API成功レスポンス<T> {
  /** 成功フラグ */
  成功: true;
  /** レスポンスデータ */
  データ: T;
  /** 成功メッセージ（オプション） */
  メッセージ?: string;
}

/**
 * APIエラーレスポンスの基本形
 */
export interface APIエラーレスポンス {
  /** 成功フラグ */
  成功: false;
  /** エラーメッセージ */
  エラー: string;
  /** エラーコード */
  エラーコード: string;
  /** 詳細エラー情報（開発用） */
  詳細?: string;
}

/**
 * APIレスポンスの共通型
 */
export type APIレスポンス<T> = API成功レスポンス<T> | APIエラーレスポンス;

/**
 * ページネーション情報
 */
export interface ページネーション {
  /** 現在のページ番号（1から開始） */
  現在ページ: number;
  /** 1ページあたりの件数 */
  ページ件数: number;
  /** 総件数 */
  総件数: number;
  /** 総ページ数 */
  総ページ数: number;
  /** 前のページがあるか */
  前ページあり: boolean;
  /** 次のページがあるか */
  次ページあり: boolean;
}

/**
 * ページネーション付きレスポンス
 */
export interface ページネーション付きレスポンス<T> {
  /** データの配列 */
  データ: T[];
  /** ページネーション情報 */
  ページ情報: ページネーション;
}