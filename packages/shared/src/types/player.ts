/**
 * プレイヤー関連の型定義
 * 初学者向け: 日本語でわかりやすく命名
 */

/**
 * プレイヤーの基本情報
 */
export interface プレイヤー {
  /** プレイヤーの一意なID */
  id: string;
  /** プレイヤー名（3-20文字） */
  名前: string;
  /** アカウント作成日時 */
  作成日時: string;
}

/**
 * 新規プレイヤー作成時のデータ
 */
export interface プレイヤー作成データ {
  /** プレイヤー名（3-20文字） */
  名前: string;
}

/**
 * プレイヤー作成後のレスポンス
 */
export interface プレイヤー作成レスポンス {
  /** 作成されたプレイヤー情報 */
  プレイヤー: プレイヤー;
  /** 初期付与されたモンスターのID */
  初期モンスターid: string;
}