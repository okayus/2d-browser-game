/**
 * スターターモンスターをIDで検索
 */
export const getスターターモンスターById = (id: string): スターターモンスター | undefined => {
  return スターターモンスター一覧.find(monster => monster.id === id);
};

/**
 * レアリティ別のスターターモンスター一覧取得
 */
export const getスターターモンスターByレアリティ = (レアリティ: string) => {
  return スターターモンスター一覧.filter(monster => monster.レアリティ === レアリティ);
};

/**
 * 種類別のスターターモンスター一覧取得
 */
export const getスターターモンスターBy種類 = (種類: string) => {
  return スターターモンスター一覧.filter(monster => monster.種類 === 種類);
};