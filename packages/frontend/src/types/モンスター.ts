/**
 * モンスターの基本情報
 */
export interface モンスター {
  /** モンスターの一意識別子 */
  id: string;
  /** モンスター名 */
  名前: string;
  /** モンスターの種類・カテゴリ */
  種類: モンスター種類;
  /** モンスターの説明文 */
  説明: string;
  /** レアリティ（希少度） */
  レアリティ: レアリティ;
  /** 基本ステータス */
  ステータス: モンスターステータス;
  /** モンスターの見た目を表す絵文字 */
  アイコン: string;
  /** 背景色（レアリティに応じた色） */
  背景色: string;
}

/**
 * モンスターの種類
 */
export type モンスター種類 = 
  | '火炎'
  | '水流' 
  | '草木'
  | '電撃'
  | '地面'
  | '風'
  | '光'
  | '闇';

/**
 * レアリティ（希少度）
 */
export type レアリティ = 
  | 'コモン'     // 一般的
  | 'アンコモン'  // やや珍しい
  | 'レア'      // 珍しい
  | 'エピック'   // とても珍しい
  | 'レジェンド'; // 超希少

/**
 * モンスターの基本ステータス
 */
export interface モンスターステータス {
  /** 体力 */
  HP: number;
  /** 攻撃力 */
  攻撃力: number;
  /** 防御力 */
  防御力: number;
  /** 素早さ */
  素早さ: number;
}

/**
 * プレイヤーが選択可能なスターターモンスターの情報
 */
export interface スターターモンスター extends モンスター {
  /** スターターとしての推奨理由 */
  推奨理由: string;
  /** 初心者へのアドバイス */
  アドバイス: string;
}

